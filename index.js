$(document).ready(function(){
     $('.btn-primary2').mouseenter(function(){
        $(this).css('background-color', '#CEE5E0');  
        $(this).css('color', '#702632');  
    });
     
    $('.btn-primary2').mouseleave(function(){
        $(this).css('background-color', '#702632');
        $(this).css('color', '#CEE5E0');   
    });
});

